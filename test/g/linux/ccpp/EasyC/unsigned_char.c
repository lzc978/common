#include <stdio.h>

/*
数值溢出
有符号溢出&无符号溢出
*/

int main()
{
    char ch; // 有符号数
    // 符号位溢出会导致数的正负发生变化
    ch = 0x7f + 2; //10进制: 127+2
    printf("%d\n", ch);
    // 127 -> 2进制原码: 0111 1111
    // 127+2 -> 2进制补码: 1000 0001, 这是负数补码, 其原码为 1111 1111, 结果是-127

    unsigned char ch2; // 无符号数
    ch2 = 0xff + 1; // 255 + 1
    printf("%u\n", ch2);
    // 255 -> 2进制原码: 1111 1111
    // 255+1 -> 1 0000 0000, char最高只有8bit, 溢出结果为 0000 0000, 10进制为0
    return 0;
}
